package com.java.beanClass;

public class Person {
	private String name;
	private int age;
	
	public Person(String name, int age) {
		this.name = name;
		this.age = age;
	}
	public String getName(String name) {
		return name;
	}
	
	public int getAge(int age) {
		return age;
	}
	
	public void setName() {
		this.name = name;
	}
	
	public void setAge() {
		this.age = age;
	}
	
	


}
